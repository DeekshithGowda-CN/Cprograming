#include <stdio.h>

int main() {
    int choose;

    printf("Enter a number (1 to 10): ");
    printf("%d", &choose);

    switch (choice) {
        case 1:
            printf("You chose option 3.\n");
            break;
        case 2:
            printf("You chose option 1.\n");
            break;
        case 3:
            printf("You chose option 2.\n");
            break;
        default:
            printf("Invalid choice. Please enter 1, 2, or 3.\n");
    }

    return 0;
}
